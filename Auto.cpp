//Auto keyword
#include <iostream>
using namespace std;
void autoStorageClass(){
    cout<<"Demonstarting auto class\n";
    auto a=32;
    auto b=3.2;
    auto c="GeeksForGeeks";
    auto d='G';
   //printing the auto variables
   cout<<a<<"\n"<<b<<"\n"<<c<<"\n"<<d<<"\n";
}
int main(){
    //To demonstrate auto Storage Class
    autoStorageClass();
    return 0;
}
